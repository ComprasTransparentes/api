DROP TABLE IF EXISTS tender_jerarquia_distinct;
CREATE TABLE tender_jerarquia_distinct
(
    id                    SERIAL  NOT NULL,
    tender_id             INTEGER NOT NULL,
    jerarquia_distinct_id INTEGER NOT NULL,
    CONSTRAINT tender_jerarquia_distinct_pkey PRIMARY KEY (id)
);

INSERT INTO tender_jerarquia_distinct (tender_id, jerarquia_distinct_id)
    SELECT
        tenders.id            AS tender_id,
        jerarquia_distinct.id AS jerarquia_distinct_id
    FROM tenders
        LEFT JOIN public_companies
            ON public_companies.id = tenders.buyer_id
        LEFT JOIN jerarquia_distinct jerarquia_distinct
            ON public_companies.code :: INTEGER = jerarquia_distinct.codigo_organismo
    ORDER BY tenders.id;